languages = (
    "arabic",
    "bengali",
    "english",
    "finnish",
    "indonesian",
    "japanese",
    "korean",
    "russian",
    "swahili",
    "telugu",
    "thai",
)


def main() -> None:
    for language_task in languages:
        file_name = f"minspan_{language_task}.yaml"
        with open(file_name, "w", encoding="utf-8") as f:
            # Manually writing the YAML-like content inside files to avoid tagging issues
            f.write("# Generated by generate.py\n")
            f.write("include: minspan_common_yaml\n")
            f.write(f"task: tydiqa_minspan_{language_task}\n")
            f.write(f"process_docs: !function utils.process_{language_task}\n")
            print(f"tydiqa_minspan_{language_task}")


if __name__ == "__main__":
    main()
